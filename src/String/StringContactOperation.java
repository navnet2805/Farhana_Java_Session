package String;

public class StringContactOperation {

    public static void main(String[] args) {

        String s = "Java";
        System.out.println(s);

        System.out.println("Java"+"Programming"); //+ operator is contcatination
        System.out.println(1+2); //+ operator is performing addition
        System.out.println("Java"+2);
        System.out.println("1"+"2");
        System.out.println(1+2+"java");
        System.out.println("Java"+1+2); //"Java12"

    }
}
