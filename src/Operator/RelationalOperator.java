package Operator;

public class RelationalOperator {
    public static void main(String[] args) {

        System.out.println(3>2);
        System.out.println(3<2);
        System.out.println(3>=1);
        System.out.println(3<=2);
        System.out.println(3==2);
        System.out.println(3!=2);

    }
}
