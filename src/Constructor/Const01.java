package Constructor;

public class Const01 {
    Const01() //default constructor
    {
    }

    Const01(int a) //1 param constructor
    {
    }

    Const01(int a , int b) //2 param constructor
    {
    }

    Const01(int a, int b , int c) //3 param constructor
    {
    }

    public static void m01()
    {
        System.out.println("from method m01");
    }

    public static void main(String[] args) {
        m01();
    }
}
